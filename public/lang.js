  // ── Public API ──────────────────────────────────────────────────────────────
  window.getLang = function () {
    const v = localStorage.getItem(LANG_KEY);
    return SUPPORTED.includes(v) ? v : "en";
  };

  window.setLang = function (l) {
    if (!SUPPORTED.includes(l)) return;
    localStorage.setItem(LANG_KEY, l);
    applyLang();
    // Also update all lang selectors on the page
    document.querySelectorAll(".jr-lang-sel").forEach(el => { el.value = l; });
  };

  window.T = function (key) {
    const l = getLang();
    return (TR[l] && TR[l][key] !== undefined ? TR[l][key] : null) ?? TR.en[key] ?? key;
  };

  window.applyLang = function () {
    document.querySelectorAll("[data-i18n]").forEach(el => {
      el.textContent = T(el.dataset.i18n);
    });
    document.querySelectorAll("[data-i18n-ph]").forEach(el => {
      el.placeholder = T(el.dataset.i18nPh);
    });
    document.querySelectorAll("[data-i18n-html]").forEach(el => {
      el.innerHTML = T(el.dataset.i18nHtml);
    });
  };

  window.buildLangSelector = function (extraClass) {
    const l = getLang();
    const opts = [
      { v: "en", label: "🇬🇧 English" },
      { v: "zh", label: "🇨🇳 中文" },
      { v: "ms", label: "🇲🇾 Melayu" },
      { v: "ta", label: "🇮🇳 தமிழ்" },
      { v: "hi", label: "🇮🇳 हिन्दी" },
      { v: "bn", label: "🇧🇩 বাংলা" },
    ];
    return `<select class="jr-lang-sel${extraClass ? " " + extraClass : ""}" onchange="setLang(this.value)">${opts.map(o => `<option value="${o.v}"${l === o.v ? " selected" : ""}>${o.label}</option>`).join("")}</select>`;
  };
